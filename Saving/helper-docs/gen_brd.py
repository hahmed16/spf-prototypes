def rtl(p, center=False):
    ppr = p._p.get_or_add_pPr()
    bidi = OxmlElement("w:bidi")
    bidi.set(qn("w:val"), "1")
    ppr.append(bidi)
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else WD_ALIGN_PARAGRAPH.RIGHT


def heading(d, t, lvl):
    p = d.add_heading(t, level=lvl)
    rtl(p)


def para(d, t, bold=False):
    p = d.add_paragraph()
    r = p.add_run(t)
    r.bold = bold
    rtl(p)


def bullets(d, items):
    for i in items:
        p = d.add_paragraph(style="List Bullet")
        p.add_run(i)
        rtl(p)


def nums(d, items):
    for i in items:
        p = d.add_paragraph(style="List Number")
        p.add_run(i)
        rtl(p)


def table(d, headers, rows):
    t = d.add_table(rows=1, cols=len(headers))
    t.style = "Table Grid"
    for i, h in enumerate(headers):
        t.rows[0].cells[i].text = h
        for p in t.rows[0].cells[i].paragraphs:
            rtl(p, True)
            for r in p.runs:
                r.bold = True
    for row in rows:
        c = t.add_row().cells
        for i, v in enumerate(row):
            c[i].text = str(v)
            for p in c[i].paragraphs:
                rtl(p)


